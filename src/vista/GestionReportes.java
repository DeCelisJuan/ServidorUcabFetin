    private void graficar(){
        ChartPanel panel;
        JFreeChart chart = null;
        if (productos!=null){
        if (radioLineas.isSelected()){
            //Grafico de lineas
            int validar=1;
            XYSplineRenderer renderer = new XYSplineRenderer();
            XYSeriesCollection dataset = new XYSeriesCollection();
            
            ValueAxis x = new NumberAxis();
            ValueAxis y = new NumberAxis();
            
            XYSeries serie = new XYSeries("Datos"); 
            
            XYPlot plot;
            lineas.removeAll();
            int maximo=0;
            
                if (productos.size()<5){
                    maximo=productos.size();
                }else{}
                    maximo=5;
                try{
                    for (int fila=0;fila<maximo;fila++){
                        serie.add(Float.parseFloat(String.valueOf(fila)),
                                 Float.parseFloat(productos.get(fila).getCantidad()));
                    }
                }catch (Exception ex){
                    validar=0;
                }
                
                if (validar==1){
                    dataset.addSeries(serie);
                    
                    x.setLabel("Eje X");
                    y.setLabel("Eje Y");
                    plot = new XYPlot(dataset,x,y,renderer);
                    chart = new JFreeChart(plot);
                    chart.setTitle("Grafico de lineas");
                }else{JOptionPane.showMessageDialog(this, "Debe llenar la tabla con datos numericos");}
            
            }else{
                if (radioBarras.isSelected()){
                    //Grafico de barras
                    DefaultCategoryDataset data = new DefaultCategoryDataset();
                    
                    String producto1 = "Juan";
                    String producto2 = "Pedro";
                    String producto3 = "ismael";
                    String producto4 = "Roberto";
                    String producto5 = "Maria";
                    String dia1="20";
                    String dia2="Dia 2";
                    String dia3="Dia 3";
                    String dia4="Dia 4";
                    
                    data.addValue(30, producto1, producto1);
                    data.addValue(50, producto2, producto2);
                    data.addValue(20, producto3, producto3);
                    data.addValue(18, producto4, producto4);
                    data.addValue(7, producto5, producto5);
                    
                    chart = ChartFactory.createBarChart("GrÃ¡fico de barras", "Empleado", "cantidad de ventas",
                                                        data, PlotOrientation.VERTICAL,
                                                        true, true, true);
                    CategoryPlot plot2 = (CategoryPlot) chart.getPlot();
                    plot2.setCrosshairDatasetIndex(50);
                    plot2.setDomainGridlinesVisible(true);
                }else{
                }if (radioPastel.isSelected()){
                    //Grafico de pastel
                    
                    DefaultPieDataset data3 = new DefaultPieDataset();
                    data3.setValue("Categoria 1", 20);
                    data3.setValue("categoria 3", 60);
                    data3.setValue("categoria 3", 20);
                    
                    chart = ChartFactory.createPieChart3D("Grafico de pastel", data3, true, true, true);
                    
                    
                }
            }
        }
        
        panel = new ChartPanel (chart);
        panel.setBounds(0,0,500,300);
        if (radioLineas.isSelected()){
            lineas.add(panel);
            
            lineas.repaint();
        }else{
            if(radioBarras.isSelected()){
                barras.add(panel);
                barras.repaint();
            }else{
                pastel.add(panel);
                pastel.repaint();
            }
        }
    }
    
    private void llenarLista(){
        productos= conexionSQL.listaProductos(true);
        Collections.sort(productos, new Comparator(){
            @Override
            public int compare(Object o1, Object o2) {
                Producto obj = (Producto) o1; Producto obj2 = (Producto) o2;
                return new Integer(obj.getCantidad()).compareTo(new Integer(obj2.getCantidad()));
            }
        });
    }
    
    
